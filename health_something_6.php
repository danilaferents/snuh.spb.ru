							Другие корейские ученые изобрели так называемый «гибкий пьезоэлектрический наногенератор». На его основе испытывают принципиально новый кардиостимулятор, не требующий источник питания. Организм сам предоставляет энергию для его работы, кардиостимулятор способен вырабатывать электрическую энергию переработкой энергии движения человеческого тела. Сейчас проводятся работы по внедрению такого генератора в организм человека. <br> <br>

