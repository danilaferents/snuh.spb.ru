							<br>
							<p>Специалистами нашей клиники достигнуты за последние годы поразительные успехи в области диагностики и лечения различных кардиоваскулярных заболеваний. И во многом этот прогресс связан со становлением такого новейшего направления, как интервенционная кардиология.</p><br>
							<p>ИНТЕРВЕНЦИОННАЯ КАРДИОЛОГИЯ - раздел клинической медицины объединяющий эндоваскулярную хирургию, рентгенологию и кардиологию, изучающий возможности диагностики и маоинвазивного лечения заболеваний сердца транскатетерными методами. Сегодня этот раздел располагает способами диагностики и лечения большинства пороков развития и заболеваний сердца, также врожденные пороки сердца, стенозы клапанов, септальные дефекты, ишемическую болезнь сердца, нарушения его ритма и проводимости. Для этого используют особые зонды и новейшее рентгенографическое оборудование. Этот способ позволяет выявлять и удалять врожденные пороки сердца,аномалии развития структуры сердца, нарушение ритма, клапанные стенозы, ишемическую болезнь, септальные аномалии и нарушения проводимости и т.д.</p><br>
							<p>Разделы: рентгеноэндоваскулярная диагностика заболеваний сердца, рентгеноэндоваскулярная хирургия пороков и заболеваний сердца, рентгеноэндоваскулярная хирургия ишемической болезни сердца (коронарных артерий), интервенционная аритмология и электрофизиология.</p><br>
							<p>Преимущества интервенционной кардиологии:</p><br>
							<ul style="margin-left: 30px;">
								<li>Малоинвазивность операций и за малые сроки;</li>
								<li>Без общего наркоза;</li>
								<li>Стационар всего несколько дней, после чего пациент возвращается к обычной жизни;</li>
								<li>Короткая реабилитация.</li>
							</ul><br>
							<p>Интервенционная кардиология - это высокая точность обследования полости сердца, динамики давления, врожденных и приобретенных дефектов, клапаннов. Возможен визуальный осмотр внешней части сердечной мышцы, так и состояния перикарда, его жидкостного наполнения через пункцию. Осматриваются стенки сосудов, их просвет, определяется наличие тромбов или атеросклеротических бляшек. Для детального обследования в сосуды вводится контрастное вещество (процедура называется коронарографией). Изображение поступает на монитор антиографа. Именно в ходе коронароангиографии специалисты решают о выборе дальнейшего лечения пациента. При использовании зонда с особым наконечником, можно делать коррекцию клапанов или устранить дефект межжелудочковой перегородки, выполнять коррекцию клапанов сердца и их замену. Для замены клапана, его в сжатом виде доставляют в сердце по катетеру, затем расправляют и закрепляют.</p><br>
							<p>ИК участвует в лечении мерцательной аритмии, пароксизмальной тахикардии, экстрасистолии. При этом используются высокочастотные радиоволны, прижигающие участок мышцы, провоцирующий нарушение ритма.</p><br>
							<p>При лечении ишемической болезни сердца (ИБС) через кожу проводят внутрисосудистую ангиопластику. Способ увеличивает артериальный просвет, сужение которого дала патологическая атеросклеротическая бляшка.</p><br>
							<p>Показания для применения:</p><br>
							<ul style="margin-left: 30px;">
								<li>симптомы ишемии;</li>
								<li>стенокардия, медикаментозное лечение которой оказалось неэффективным;</li>
								<li>артерии сердца в хорошем состоянии, патология обнаружена только в одном или двух сосудах;</li>
								<li>повторная стенокардия после перенесенного инфаркта миокарда.</li>
							</ul><br>
							<p>Часто применяется методика эндоваскулярной реваскуляризации сердечной мышцы, иначе баллонная транслюминальная ангиопластика. Выполняют ее в нашем кардиоцентре исключительно высококвалифицированные врачи – интервенционные кардиологи.  Баллонную ангиопластика осуществляют через бедренную артерию с введением баллона, заполненного контрастным веществом. Через катетер его доставляют в место сужения сосуда и раздувают, расширяя просвет.</p><br>
							<p>При полной закупорке просвета сосуда, проводится ротационная атерэктомия. Используется специальный бур, покрытый алмазными кристаллами. Который разрушает бляшку и восстанавливает просвет сосуда.</p><br>
							<p>Для обеспечения более стойкого, длительного эффекта описанную процедуру баллонной ангиопластики дополняют коронарным стентированием, это в значительно снижает риск повторного сужения сосуда (рестеноза). Введение эндоваскулярного стента, представляющего собой трубку, изготовленную из биосовместимого надежного материала, имеющую ячеистую структуру, выполняется также с помощью катетера с баллончиком на конце, который подводят к месту сужения коронарной артерии. При раздувании баллончика стент расширяется и плотно вжимается в сосудистую стенку, увеличивая, просвет стенозированного сосуда. Для полной уверенности в правильной установке стента, баллон можно раздувать несколько раз, в конце его сдувают и удаляют, стент оставляют.</p><br>
							<p>Если длина стенозированного участка большая,можно установить несколько стентов. Также в нашей клинике практикуют имплантацию последней генерации коронарных стентов со специальным лекарственным покрытием, оно служит эффективной мерой профилактики рестеноза сосудов при их тяжелых поражениях атеросклерозом. Почти всех пациентов из стационара выписывают через сутки после коронарного стентирования.</p><br>
