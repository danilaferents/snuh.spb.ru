			<p class="_title">Миниинвазивная хирургия в онкологии</p><br>
			<p>В последние десятилетия хирургия злокачественных новообразований претерпела значительные изменения в лучшую сторону. Случилось это не только в связи с развитием медицинских технологий. Роль технического прогресса в развитии медицины и хирургии неоценима, но не меньшее значение имеет тенденция к гуманизации и индивидуализации в лечении онкобольных. Обнаружение рака перестало восприниматься как приговор и предстоящее мучительное и калечащее тело и душу лечение. Так, если каких-то 50 лет назад единственным значительным критерием онкологического вмешательства являлся его радикализм, то сегодня, наряду с радикальностью операции, не меньшее значение приобретает и качество дальнейшей жизни пациента. Некоторые авторы объединяют данный факт с общим значительным увеличением выживаемости онкобольных и возрастающим вниманием к качеству их жизни. Неоспоримо, что прогресс в этом вопросе базируется на 3 основных составляющих: миниинвазивные хирургические технологии, прецизионность вмешательства и органосохраняющие операции. В последние годы по всем этим составляющим удалось добиться значительного прогресса. Особого внимания заслуживают такие направления, как миниинвазивные и эндовидеохирургические технологии, хирургия однопортовых доступов (LESS или SILS), роботассистированные вмешательства (RALS), транслюминальные доступы (NOTES) и эндоскопические вмешательства, а также биопсия «сторожевого» лимфоузла (Sentinel node biopsy) и идентификация опухолевой ткани.</p>
			<p class="_title">Миниинвазивные хирургические технологии</p><br>
			<p>Миниинвазивная хирургия (minimal access surgery – MAS) для онкологических заболеваний приобрела свою актуальность гораздо позже, чем в других видах хирургии. Это связано с довольно трудоемкими и долгосрочными исследованиями, с необходимыми специфическими для онкологии показателями. Также, учитывая большой объем резецируемых тканей и технически более сложные вмешательства по сравнению с общехирургическими, кривая обучения миниинвазивным методикам в онкологии оказывается не слишком очевидной. В общем, основные направления развития миниинвазивных хирургических технологий ведут к нескольким аспектам: совершенствование эндоскопических резекций, увеличение спектра эндоскопических, эндовидеохирургических и видеоассистированных вмешательств; развитие хирургии монопортовых (laparoendoscopic siglesite surgery – LESS) и транслюминальных (natural orifice transluminal endoscopic surgery – NOTES) доступов; внедрение робототехники (robot assisted laparoscopic surgery – RALS). Последние три направления (LESS, NOTES и RALS), по мнению О. Alimoglu (2013 г.), относятся к конечным точкам развития миниинвазивной хирургии.</p>
